# variabel biasa digunakan untuk mendeklarasikan suatu data

# penggunaan variabel
nama = 'adinata' # kata 'nama' merupakan variabel yang memiliki data yaitu 'adinata'
print (nama) # mencetak isi variabel dengan memanggil variabelnya yaitu 'nama'


# penggunaan variabel dalam operasi aritmatika
angka_1 = 20
angka_2 = 50

hasil = angka_1 + angka_2 #membuat variabel 'hasil untuk menambahkan variabel 'angka_1' dan 'angka_2'
print (hasil) # mencetak hasil dari penjumlahan dengan memanggil variabel 'hasil'

